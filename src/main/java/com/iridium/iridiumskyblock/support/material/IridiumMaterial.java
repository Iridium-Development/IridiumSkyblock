    private static Map<String, IridiumMaterial> materials = new HashMap<>();
    @Getter
    private String key;
    @Getter
    private String extra;

    public static IridiumMaterial remove(String s) {
        IridiumMaterial im = materials.get(s);
        materials.remove(s);
        return im;
    }

    public static IridiumMaterial add(IridiumMaterial im) {
        materials.put(im.getKey(), im);
        return im;
    }

    public static IridiumMaterial valueOf(String materialString) {
        if (materialString == null)
            return null;
        String key = materialString;
        if (materials.containsKey(key)) {
            return materials.get(key);
        }
        int indexOfExtra = materialString.indexOf("_OF");
        if (indexOfExtra == -1)
            indexOfExtra = materialString.indexOf("_NAMED");

        String extra = "";
        if (indexOfExtra != -1) {
            extra = materialString.substring(indexOfExtra);
            materialString = materialString.substring(0, indexOfExtra);
        }
        materialString = materialString.toUpperCase();

        Material mat = Material.getMaterial(materialString);
        if (mat != null) {
            materials.put(key, new BukkitMaterial(mat, key, extra));
            return materials.get(key);
        }
        if (materialString.endsWith("_SPAWNER")) {
            String entityString = materialString.replaceAll("_SPAWNER", "");
            try {
                EntityType t = EntityType.valueOf(entityString);
                if (t != null) {
                    materials.put(key, new SpawnerMaterial(t, key, extra));
                    return materials.get(key);
                }
            } catch (IllegalArgumentException noEntityException) {
                // Just ignore it and try parsing it with XMaterial instead
            }
        }
        Optional<XMaterial> xMaterial = XMaterial.matchXMaterial(materialString);
        if (xMaterial.isPresent()) {
            materials.put(key, new XMaterialImpl(xMaterial.get(), key, extra));
            return materials.get(key);
        }

        return null;
    }

    public static Optional<IridiumMaterial> matchXMaterial(String string) {
        return Optional.of(valueOf(string));
    }

    public static IridiumMaterial matchXMaterial(Material mat) {
        return valueOf(mat.name());
    }

    public static List<IridiumMaterial> values() {
        return new ArrayList<>(materials.values());
    }

    public String name() {
        return key;
    }

    public String name(IridiumMaterial mat) {
        return mat.key;
    }

    public abstract Material parseMaterial();

    public abstract ItemStack parseItem();

    protected void applyExtra(ItemStack is) {
        if (extra == null || extra.length() == 0)
            return;
        String name = null;
        String workSpace = extra;
        int namedIndex = extra.toUpperCase().indexOf("_NAMED_");
        if (namedIndex >= 0) {
            name = workSpace.substring(namedIndex + 7);
            workSpace = workSpace.substring(0, namedIndex);
        }

        // OF_ENCHANT_LEVEL

        int ofIndex = workSpace.toUpperCase().indexOf("_OF_");
        while (ofIndex >= 0) {
            int indexStart = ofIndex + 4;
            ofIndex = workSpace.toUpperCase().indexOf("_OF_", indexStart);
            String enchantString = workSpace.substring(indexStart, Math.max(ofIndex, workSpace.length()));

            int level = 1;
            String[] elements = enchantString.split("_");
            boolean isNumeric = elements[elements.length - 1].matches("-?\\d+?");
            if (isNumeric) {
                enchantString = enchantString.substring(0, enchantString.lastIndexOf("_"));
                level = Integer.parseInt(elements[elements.length - 1]);
            }

            Enchantment enchant = Enchantment.getByName(enchantString);
            if (enchant != null) {
                is.addUnsafeEnchantment(enchant, level);
            }
        }

        // NAMED X
        if (name != null) {
            ItemMeta itemMeta = is.getItemMeta();
            if (itemMeta != null) {
                IridiumSkyblock.getInstance().getLogger().info("Naming item as " + name);
                itemMeta.setDisplayName(name);
                is.setItemMeta(itemMeta);
            }
        }
    }

    public boolean isSimilar(ItemStack item) {
        return parseItem().isSimilar(item);
    }

